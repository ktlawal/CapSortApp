    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            //Retrieves IP Address and Port Number from GUI textfields and create a new socket to communicate with Server
            int portNumber = Integer.parseInt(jTextField3.getText());
            socket = new Socket(jTextField2.getText(), portNumber);
            
            // Input and output streams from and to the server
            ObjectOutputStream outToServer = new ObjectOutputStream(socket.getOutputStream());
            ObjectInputStream inFromServer = new ObjectInputStream(socket.getInputStream());

            //Frame components and Server connection establishment
                message = (Message) inFromServer.readObject();
                jTextArea1.append(message.getMessage() + "\n");
                jTextArea1.append("\n");
                jTextArea2.setText(message.getTrackClients());
                jTextArea3.setText(message.getClients());
                jButton1.setText("Connected");
                jButton1.setBackground(Color.green);
                jButton1.setEnabled(false);

        //Connection Exception handling       
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(CapSortClientGUI.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

