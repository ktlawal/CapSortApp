    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            //Reads input streams and write output streams from and to the server
            ObjectOutputStream outToServer = new ObjectOutputStream(socket.getOutputStream());
            ObjectInputStream inFromServer = new ObjectInputStream(socket.getInputStream());

            //Write Message Object to Server
            message.setMessage(jTextField5.getText());
            outToServer.writeObject(message);
            
            //Read Message Object Message from Server
            message = (Message)inFromServer.readObject();

            //Set Object properties to display  
            jTextArea1.append("\nResponse from Server:\n");
            jTextArea1.append(message.getMessage());
            jTextArea2.setText(message.getTrackClients());
            jTextArea3.setText(message.getClients());

            jTextArea1.append("\n");
            jTextField5.selectAll();
            jTextField5.setText("");

        } catch (Exception e) {
            Logger.getLogger(CapSortClientGUI.class.getName()).log(Level.SEVERE, null, e);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

